
class ConverterCupsToGrams{
    public static void main (String [] args){
        double numberGrams;
        double numberCups;

        numberCups = 3.0/4;

        numberGrams = numberCups * 240;

        System.out.println(
            "The equivalent of " + numberCups + " cups in grams is " + numberGrams + " grs.");   

    }
}